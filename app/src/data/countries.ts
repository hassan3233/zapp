// Turn an ISO alpha-2 code into its flag emoji (regional indicator letters).
export function flagEmoji(code: string): string {
  const cc = code.toUpperCase();
  if (!/^[A-Z]{2}$/.test(cc)) return "🏳️";
  return String.fromCodePoint(
    ...[...cc].map((c) => 0x1f1e6 + c.charCodeAt(0) - 65)
  );
}

export function countryByCode(code?: string | null): Country | undefined {
  if (!code) return undefined;
  const cc = code.toUpperCase();
  return COUNTRIES.find((c) => c.code === cc);
}

// Pick a sensible default for a device region (e.g. "IQ"), falling back to US.
export function defaultCountry(region?: string | null): Country {
  return countryByCode(region) || countryByCode("US")!;
}
